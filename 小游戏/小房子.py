import turtle
t=turtle.Pen()
t.shape("turtle")
t.speed(0)
t.pensize(3)
#墙
t.setheading(90)
t.pencolor("burlywood")
t.fillcolor("Navajowhite")
t.begin_fill()
for a in range (2):
    t.forward(140)
    t.right(90)
    t.forward(60)
    t.right(90)
t.end_fill()
#屋顶
t.fillcolor("skyblue")
t.begin_fill()
t.forward(140)
t.circle(-30,180)
t.setheading(180)
t.forward(60)
t.end_fill()
t.up()
t.goto(10,90)
t.down()
t.fillcolor("dodgerblue")
t.begin_fill()
t.right(180)
for a in range(4):
    t.forward(40)
    t.right(90)
t.up()
t.goto(10,70)
t.down()
t.end_fill()
#窗子
t.pencolor("lightpink")
t.forward(40)
t.up()
t.goto(30,90)
t.down()
t.right(90)
t.forward(40)
t.up()
t.goto(-60,0)
t.down()
t.setheading(90)
#房（2）
t.fillcolor("lightcoral")
t.begin_fill()
for a in range (2):
    t.forward(70)
    t.right(90)
    t.forward(60)
    t.right(90)
t.end_fill()
t.forward(70)
t.pencolor("palegoldenrod")
t.fillcolor("Palegreen")
t.begin_fill()
t.setheading(60)
t.forward(60)
t.setheading(-60)
t.forward(60)
t.end_fill()
t.up()
t.goto(-48,30)
t.down()
t.fillcolor("darkorange")
t.begin_fill()
t.circle(20)
t.end_fill()
t.up()
t.goto(-47,40)
t.down()
t.setheading(0)
t.pencolor("thidtle")
t.forward(34)
t.up()
t.goto(-30,57)
t.down()
t.right(90)
t.forward(34)
#房（3）
t.up()
t.goto(62,60)
t.down()
t.fillcolor("orchid")
t.begin_fill()
for a in range(4):
    t.forward(60)
    t.left(90)
t.end_fill()
t.setheading(60)
t.fillcolor("yellow")
t.begin_fill()
t.forward(30)
t.right(60)
t.forward(31)
t.right(60)
t.forward(30)
t.end_fill()
t.up()
t.goto(75,30)
t.down()
t.setheading(90)
t.fillcolor("salmon")
t.begin_fill()
t.circle(-17,steps=8)
t.end_fill()
